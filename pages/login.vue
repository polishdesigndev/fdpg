          showVerifySuccess(this.$refs) // 驗證成功動畫
          setTimeout(() => {
            this.verifyMsg = '審核成功！'
          }, 800)
        }, 1000)
        setTimeout(() => {
          setUser(JSON.stringify(resp)) // 儲存
          this.$router.push('/') // 跳轉
          // 開始輪詢上線人數
          this.poll()
        }, 3000)
      } catch (error) {
